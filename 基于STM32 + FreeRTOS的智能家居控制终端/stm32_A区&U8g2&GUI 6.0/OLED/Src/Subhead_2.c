void ShowMatrix(u8g2_t *u8g2)
{
	g_xQueueMenu = xQueueCreate(1,sizeof(struct Key));
	struct Key	key_data;
	
	u8g2_SetFontPosTop(u8g2);
	
	while(1)
	{
		u8g2_ClearBuffer(u8g2);
		AHT10_Read_Data(&AttributeCB.tempdata,&AttributeCB.humidata);                               //뗍혤侊嫁똑令
		Start_BH1750();	                                                              //꽃섞瘻뻣
		vTaskDelay(1000);
		AttributeCB.lightata = Read_BH1750()/1.2;                           	  	  //뗍혤깻셕炬鑒令
		
		
		u8g2_DrawXBMP(u8g2,0,0,16,16,WENG);
		u8g2_DrawXBMP(u8g2,32,0,16,16,DU);
		u8g2_DrawStr(u8g2, 48, 0, ":");
		sprintf(Temperature_str, "%.2f", AttributeCB.tempdata); // 괏즛2貫鬼鑒
		u8g2_DrawStr(u8g2, 60, 0, Temperature_str);
		u8g2_DrawGlyph(u8g2,100, 0, 0x00B0);
		u8g2_DrawStr(u8g2, 106, 0, "C");
		
		u8g2_DrawXBMP(u8g2,0,16,16,16,SHI);
		u8g2_DrawXBMP(u8g2,32,16,16,16,DU);
		u8g2_DrawStr(u8g2, 48, 16, ":");
		sprintf(Humidity_str, "%.2f", AttributeCB.humidata); // 괏즛2貫鬼鑒
		u8g2_DrawStr(u8g2, 60, 16, Humidity_str);
		u8g2_DrawStr(u8g2, 100, 16, "%");
		
		u8g2_DrawXBMP(u8g2,0,32,16,16,GUANG);
		u8g2_DrawXBMP(u8g2,16,32,16,16,QIANG);
		u8g2_DrawXBMP(u8g2,32,32,16,16,DU);
		u8g2_DrawStr(u8g2, 48, 32, ":");
		sprintf(Light_str, "%.0f", AttributeCB.lightata); 
		u8g2_DrawStr(u8g2, 60, 32, Light_str);
		u8g2_DrawStr(u8g2, 100, 32, "Lux");
		
		u8g2_DrawXBMP(u8g2,0,48,16,16,ZHI);
		u8g2_DrawXBMP(u8g2,16,48,16,16,WAI);
		u8g2_DrawXBMP(u8g2,32,48,16,16,XIAN);
		u8g2_DrawXBMP(u8g2,48,48,16,16,QIANG);
		u8g2_DrawXBMP(u8g2,64,48,16,16,DU);
		u8g2_DrawStr(u8g2, 80, 48, ":");
		s12sd_Read_Data();
		sprintf(Ultraviolet_str, "%d",UV_Index(average));
		u8g2_DrawStr(u8g2, 92, 48, Ultraviolet_str);
		u8g2_DrawXBMP(u8g2, 110, 48,16,16, JI);
		
		u8g2_SendBuffer(u8g2);
		if(pdPASS ==  xQueueReceive(g_xQueueMenu,&key_data,0))
		{
			if(key_data.Key_Data == CM_KEY && key_data.Key_Param == VK_CLICK)
			{
				
			}
			else if(key_data.Key_Data == CM_KEY && key_data.Key_Param == VK_DOUBLE)
			{
				LinearClearScreen(u8g2, CLEAR_LEFT, 10, 10);
				vTaskResume(xShowTimeTaskHandle);
				vTaskSuspend(NULL);
			}
			else if(key_data.Key_Data == CM_KEY && key_data.Key_Param == VK_LONG)
			{
				LinearClearScreen(u8g2, CLEAR_LEFT, 10, 10);
				vTaskResume(xShowTimeTaskHandle);
				vTaskSuspend(NULL);
			}
			else if(key_data.Key_Data == CM_ROATATING && key_data.Key_Param == EK_RIGHT)
			{
				
			}
			else if(key_data.Key_Data == CM_ROATATING && key_data.Key_Param == EK_LEFT)
			{
				
			}
		}
	}
}

void ShowMatrixTask(void *params)
{
	ShowMatrix(&u8g2);
}





